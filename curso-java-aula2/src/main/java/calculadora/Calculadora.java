package calculadora;

public class Calculadora {
	
	public double somar(double valor1, double valor2){ // soma 
		
		return valor1 + valor2;
	}
	
	
	public double subtrair(double valor1, double valor2){ // subtrair
		
		return valor1 - valor2;
	}
	
	
   public double dividir(double valor1, double valor2){// dividir
		
		return valor1 / valor2;
	}
   
   
   public double multiplicacao(double valor1, double valor2){//multiplica√ßao
		
		return valor1 * valor2;
	}

}
